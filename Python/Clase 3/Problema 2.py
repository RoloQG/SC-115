# Calculadora simple

numero1 = float(input("Ingrese el primer número:"))
numero2 = float(input("Ingrese el segundo número:"))

suma = numero1+numero2
resta = numero1-numero2
multiplicacion = numero1*numero2
division = numero1/numero2

print("El valor de la suma de ", numero1,"+", numero2, "es:",suma)
print("El valor de la resta de ", numero1,"-", numero2, "es:",resta)
print("El valor de la multiplicación de ", numero1,"*", numero2, "es:",multiplicacion)
print("El valor de la división de ", numero1,"/", numero2, "es:",division)


